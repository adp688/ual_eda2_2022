package pruebaDelEsca;

public class Tesoro {
	
	private int peso;
	private int valor;
	
	public Tesoro(int peso, int valor) {
		this.peso = peso;
		this.valor = valor;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

}
